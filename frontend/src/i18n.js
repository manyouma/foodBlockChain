function translateZH(name) {
  if (!name) return name;
  if (ZH_LOCATIONS[name]) return ZH_LOCATIONS[name];
  // Try stripping ", Province/Country" suffix
  const base = name.replace(/,\s*(Guangdong|China|广东|中国)$/i, "").trim();
  if (ZH_LOCATIONS[base]) return ZH_LOCATIONS[base];
  return name;
}

